let javascriptIsFun = true;
console.log(javascriptIsFun);

console.log(typeof true);
console.log(typeof javascriptIsFun);
console.log(typeof 23);
console.log(typeof "Jonas");

// This is an example of Dynamic Typing. We are changing the value of the variable javascriptIsFun.
javascriptIsFun = "YES!";
console.log(javascriptIsFun);

// creating a variable that's undefined
let year;
console.log(year);
console.log(typeof year);

year = 1991;
console.log(year);
